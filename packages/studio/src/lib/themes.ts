export function getTheme(id: string, mode: 'light' | 'dark' = 'light'): Theme {
  const themeCollection = mode === 'dark' ? darkThemes : themes
  return themeCollection[id] || themeCollection.default
}

export function applyTheme(theme: Theme) {
  const root = document.documentElement

  // Add transition class for smooth color changes
  root.classList.add('theme-transitioning')

  // Apply colors directly - the theme data is in HSL format
  // The CSS will convert to oklch() format automatically
  Object.entries(theme.colors).forEach(([key, value]) => {
    if (value !== undefined) {
      root.style.setProperty(`--${key}`, value)
    }
  })

  // Apply radius
  if (theme.radius) {
    root.style.setProperty('--radius', theme.radius)
  }

  // Apply fonts
  if (theme.fonts) {
    if (theme.fonts.sans) {
      root.style.setProperty('--font-sans', theme.fonts.sans)
    }
    if (theme.fonts.serif) {
      root.style.setProperty('--font-serif', theme.fonts.serif)
    }
    if (theme.fonts.mono) {
      root.style.setProperty('--font-mono', theme.fonts.mono)
    }
  }

  // Apply shadows
  if (theme.shadows) {
    if (theme.shadows.color) {
      root.style.setProperty('--shadow-color', theme.shadows.color)
    }
    if (theme.shadows.opacity) {
      root.style.setProperty('--shadow-opacity', theme.shadows.opacity)
    }
    if (theme.shadows.blur) {
      root.style.setProperty('--shadow-blur', theme.shadows.blur)
    }
    if (theme.shadows.spread) {
      root.style.setProperty('--shadow-spread', theme.shadows.spread)
    }
    if (theme.shadows['offset-x']) {
      root.style.setProperty('--shadow-offset-x', theme.shadows['offset-x'])
    }
    if (theme.shadows['offset-y']) {
      root.style.setProperty('--shadow-offset-y', theme.shadows['offset-y'])
    }
  }

  // Remove transition class after animation completes
  setTimeout(() => {
    root.classList.remove('theme-transitioning')
  }, 300)
}

export function getAllThemes(mode: 'light' | 'dark' = 'light'): Theme[] {
  const themeCollection = mode === 'dark' ? darkThemes : themes
  return Object.values(themeCollection)
}

export function getThemeIds(mode: 'light' | 'dark' = 'light'): string[] {
  const themeCollection = mode === 'dark' ? darkThemes : themes
  return Object.keys(themeCollection)
}

/**
 * Convert HSL color string to CSS hsl() format
 * @param hslString - HSL string in format "H S% L%"
 * @returns CSS hsl() string
 */
export function hslToCss(hslString: string): string {
  const [h, s, l] = hslString.split(' ').map(str => parseFloat(str))
  return `hsl(${h}, ${s}%, ${l}%)`
}

/**
 * Get color value from theme in CSS format
 * @param theme - Theme object
 * @param colorKey - Color key name
 * @returns CSS color string
 */
export function getThemeColorCss(theme: Theme, colorKey: keyof Theme['colors']): string | null {
  const colorValue = theme.colors[colorKey]
  if (!colorValue) return null
  return hslToCss(colorValue)
}
